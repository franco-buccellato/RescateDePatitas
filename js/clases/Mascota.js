export class Mascota {
    constructor ( 
        tipoMascota,
        nombre,
        apodo,
        edadAproximada,
        sexo,
        raza,
        descripcionFisica,
        fotos,
        estado
    ) {
        this.tipoMascota = tipoMascota;
        this.nombre = nombre;
        this.apodo = apodo;
        this.edadAproximada = edadAproximada;
        this.sexo = sexo;
        this.raza = raza;
        this.descripcionFisica = descripcionFisica;
        this.fotos = fotos;
        this.estado = estado;
    }
}